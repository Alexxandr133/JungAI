type StageId = 'S1' | 'S2' | 'S3' | 'S4' | 'S5' | 'S6';

type IndividuationOption = {
  key: 'A' | 'B' | 'C' | 'D';
  text: string;
  scores: { stage: StageId; state: 'D' | 'F' | 'I'; weight?: number }[];
};

export type IndividuationQuestionBankItem = {
  id: number;
  stage: StageId;
  text: string;
  options: IndividuationOption[];
};

function opts(stage: StageId, a: string, b: string, c: string, d: string): IndividuationOption[] {
  return [
    { key: 'A', text: a, scores: [{ stage, state: 'D', weight: 2 }] },
    { key: 'B', text: b, scores: [{ stage, state: 'F', weight: 2 }] },
    { key: 'C', text: c, scores: [{ stage, state: 'I', weight: 2 }] },
    { key: 'D', text: d, scores: [{ stage, state: 'D', weight: 1 }] },
  ];
}

function sq(id: number, stage: StageId, title: string, scenario: string, a: string, b: string, c: string, d: string): IndividuationQuestionBankItem {
  return {
    id,
    stage,
    text: `${title}\n${scenario}`,
    options: opts(stage, a, b, c, d),
  };
}

