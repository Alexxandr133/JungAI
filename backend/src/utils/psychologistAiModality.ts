export function normalizePsychologistModality(raw: unknown): PsychologistAiModalityId {
  const s = typeof raw === 'string' ? raw : '';
  if (PSYCHOLOGIST_AI_MODALITIES.includes(s as PsychologistAiModalityId)) return s as PsychologistAiModalityId;
  return 'jungian_analytical';
}

export function buildGeneralModalityPrompt(modality: PsychologistAiModalityId): string {
  return GENERAL[modality] ?? GENERAL.jungian_analytical;
}

export function buildClientModalityPrompt(modality: PsychologistAiModalityId): string {
  return (CLIENT_INTRO[modality] ?? CLIENT_INTRO.jungian_analytical) + CLIENT_SUFFIX;
}

export type ResponseStyle = 'concise' | 'balanced' | 'detailed';

export function appendResponseStyle(systemPrompt: string, style: unknown): string {
  const s = style === 'concise' || style === 'detailed' || style === 'balanced' ? style : 'balanced';
  if (s === 'balanced') return systemPrompt;
  if (s === 'concise') {
    return `${systemPrompt}\n\nСтиль ответа: кратко, по пунктам, без лишней воды.`;
  }
  return `${systemPrompt}\n\nСтиль ответа: развёрнуто, с примерами и при необходимости уточняющими вопросами.`;
}

export function clampAiTemperature(raw: unknown): number {
  const n = typeof raw === 'number' ? raw : Number(raw);
  if (!Number.isFinite(n)) return 0.7;
  return Math.min(1, Math.max(0, n));
}

/** Длина ответа не задаётся пользователем: подбираем потолок по «объёму ответа». */
export function inferMaxTokensFromResponseStyle(style: ResponseStyle): number {
  switch (style) {
    case 'concise':
      return 2048;
    case 'balanced':
      return 3072;
    case 'detailed':
      return 4096;
    default:
      return 3072;
  }
}

/** Текст «кто психолог / как отвечать» — добавляется к системному промпту. */
export function appendPersonalization(systemPrompt: string, raw: unknown): string {
  const t = typeof raw === 'string' ? raw.trim() : '';
  if (!t) return systemPrompt;
  return `${systemPrompt}\n\nПерсонализация от психолога (учитывай при каждом ответе):\n${t}`;
}
